def emit_swift(name: str, data: list[tuple[float, float, float]]) -> str:
    lines = [f"    static let {name}LUT: [SIMD4<Float>] = ["]
    for i, (r, g, b) in enumerate(data):
        comma = "," if i < len(data) - 1 else ""
        lines.append(f"        SIMD4<Float>({r:.6f}, {g:.6f}, {b:.6f}, 1.0){comma}")
    lines.append("    ]")
    return "\n".join(lines)


def main():
    # Pad both to exactly 256 entries
    viridis = VIRIDIS[:256]
    magma = MAGMA[:256]

    while len(viridis) < 256:
        viridis.append(viridis[-1])
    while len(magma) < 256:
        magma.append(magma[-1])

    output = """import simd

/// Pre-computed colormap lookup tables for the spectrogram GPU pipeline.
/// Generated by scripts/generate_colormaps.py — do not edit manually.
enum ColormapData {
"""
    output += emit_swift("viridis", viridis)
    output += "\n\n"
    output += emit_swift("magma", magma)
    output += "\n}\n"

    outpath = "Wavelength/Core/ColormapData.swift"
    with open(outpath, "w") as f:
        f.write(output)
    print(f"Written {outpath} ({len(viridis)} viridis + {len(magma)} magma entries)")


if __name__ == "__main__":
    main()
